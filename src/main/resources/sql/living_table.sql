-- 5. 월별 수입 수기 등록
CREATE TABLE IF NOT EXISTS LIVING_INCOME_MST (
    INCOME_SEQ  BIGINT       AUTO_INCREMENT PRIMARY KEY,
    FAMILY_ID   VARCHAR(30)  NOT NULL,
    CC_SEQ      BIGINT       NOT NULL               COMMENT 'FK → COST_CENTER_MST',
    INCOME_YYMM CHAR(6)      NOT NULL               COMMENT '년월 YYYYMM',
    ACTUAL_AMT  BIGINT       NOT NULL DEFAULT 0     COMMENT '실제 수입금액',
    MEMO        VARCHAR(500),
    REG_ID      VARCHAR(30),
    REG_DT      DATETIME     NOT NULL DEFAULT NOW(),
    UPD_ID      VARCHAR(30),
    UPD_DT      DATETIME,
    UNIQUE KEY UK_LIVING_INCOME (FAMILY_ID, CC_SEQ, INCOME_YYMM)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX IF NOT EXISTS IDX_LIVING_INCOME_FAMILY ON LIVING_INCOME_MST (FAMILY_ID, INCOME_YYMM);

-- 3. 월별 생활비 실적 헤더
CREATE TABLE IF NOT EXISTS LIVING_EXPENSE_MST (
    EXP_SEQ   BIGINT      AUTO_INCREMENT PRIMARY KEY,
    FAMILY_ID VARCHAR(30) NOT NULL,
    EXP_YYMM  CHAR(6)     NOT NULL COMMENT '년월 YYYYMM',
    MEMO      VARCHAR(500),
    REG_ID    VARCHAR(30),
    REG_DT    DATETIME    NOT NULL DEFAULT NOW(),
    UPD_ID    VARCHAR(30),
    UPD_DT    DATETIME,
    UNIQUE KEY UK_LIVING_EXP (FAMILY_ID, EXP_YYMM)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 4. 월별 생활비 실적 상세 (항목별 실제 지출)
CREATE TABLE IF NOT EXISTS LIVING_EXPENSE_DTL (
    DTL_SEQ    BIGINT  AUTO_INCREMENT PRIMARY KEY,
    EXP_SEQ    BIGINT  NOT NULL,
    ITEM_SEQ   BIGINT  NOT NULL,
    ACTUAL_AMT BIGINT  NOT NULL DEFAULT 0,
    MEMO       VARCHAR(500),
    REG_ID     VARCHAR(30),
    REG_DT     DATETIME NOT NULL DEFAULT NOW(),
    UPD_ID     VARCHAR(30),
    UPD_DT     DATETIME,
    UNIQUE KEY UK_LIVING_DTL (EXP_SEQ, ITEM_SEQ)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

