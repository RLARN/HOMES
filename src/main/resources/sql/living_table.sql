-- 1. 생활비 카테고리
CREATE TABLE IF NOT EXISTS LIVING_BUDGET_CAT (
    CAT_SEQ    BIGINT       AUTO_INCREMENT PRIMARY KEY,
    FAMILY_ID  VARCHAR(30)  NOT NULL,
    CAT_NM     VARCHAR(100) NOT NULL,
    SORT_ORDER INT          NOT NULL DEFAULT 0,
    USE_YN     CHAR(1)      NOT NULL DEFAULT 'Y',
    REG_ID     VARCHAR(30),
    REG_DT     DATETIME     NOT NULL DEFAULT NOW(),
    UPD_ID     VARCHAR(30),
    UPD_DT     DATETIME
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX IF NOT EXISTS IDX_LIVING_CAT_FAMILY ON LIVING_BUDGET_CAT (FAMILY_ID, USE_YN);

-- 2. 생활비 항목 (기준정보)
CREATE TABLE IF NOT EXISTS LIVING_BUDGET_ITEM (
    ITEM_SEQ        BIGINT       AUTO_INCREMENT PRIMARY KEY,
    FAMILY_ID       VARCHAR(30)  NOT NULL,
    CAT_SEQ         BIGINT       NOT NULL,
    ITEM_NM         VARCHAR(100) NOT NULL,
    BUDGET_AMT      BIGINT       NOT NULL DEFAULT 0,
    INCOME_PLAN_SEQ BIGINT       COMMENT 'FK → CASH_FLOW_PLAN_MST (수입원)',
    SORT_ORDER      INT          NOT NULL DEFAULT 0,
    USE_YN          CHAR(1)      NOT NULL DEFAULT 'Y',
    DEL_YN          CHAR(1)      NOT NULL DEFAULT 'N',
    MEMO            VARCHAR(500),
    REG_ID          VARCHAR(30),
    REG_DT          DATETIME     NOT NULL DEFAULT NOW(),
    UPD_ID          VARCHAR(30),
    UPD_DT          DATETIME
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE INDEX IF NOT EXISTS IDX_LIVING_ITEM_FAMILY ON LIVING_BUDGET_ITEM (FAMILY_ID, CAT_SEQ, DEL_YN);

